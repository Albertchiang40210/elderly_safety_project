#!/bin/bash

# =====================================================================
# 🏥 智慧安養機構專案 - 一鍵完美點火腳本
# 資展國際 第三組期末專題 (FastAPI + Streamlit + YOLOv8 + MySQL)
# =====================================================================

PROJECT_DIR="/Users/albert/Documents/專案/elderly_safety_project"
echo "🚀 正在啟動智慧安養監控系統，準備接管戰場..."
cd "$PROJECT_DIR"

# 🛡️ 安全防禦：先強制把之前可能殘留、卡在背景的 8000 和 8501 連接埠通通殺掉，防止佔用
echo "🧹 清理舊有的殘留連線..."
kill -9 $(lsof -t -i:8000) 2>/dev/null
kill -9 $(lsof -t -i:8501) 2>/dev/null

# ---------------------------------------------------------------------
# 🚀 第一步：背景點火 FastAPI 後端（Uvicorn）
# ---------------------------------------------------------------------
echo "1️⃣ 正在背景點火 FastAPI 核心主機 (Port 8000)..."
source venv/bin/activate
nohup uvicorn backend.main:app --host 127.0.0.1 --port 8000 --reload > backend.log 2>&1 &

# 🛌 關鍵緩衝：原地躺 2 秒，等待 FastAPI 與 MySQL 資料庫完成握手連線
sleep 2

# ---------------------------------------------------------------------
# 🚀 第二步：背景點火 ngrok 安全隧道
# ---------------------------------------------------------------------
echo "2️⃣ 正在背景點火 ngrok 公網對接隧道..."
nohup ngrok http 8000 --domain=uncrown-pacific-sprout.ngrok-free.dev > ngrok.log 2>&1 &

# ---------------------------------------------------------------------
# 🚀 第三步：背景點火 Streamlit 護理站前端
# ---------------------------------------------------------------------
echo "3️⃣ 正在背景點火 Streamlit 監控中心網頁 (Port 8501)..."
nohup streamlit run frontend_app.py --server.port 8501 > streamlit.log 2>&1 &

# 🛌 緩衝 1 秒，確保網頁伺服器就位
sleep 1

# ---------------------------------------------------------------------
# 🚀 第四步：前景執行 AI 核心偵測（留在主畫面上讓你即時看日誌）
# ---------------------------------------------------------------------
echo "4️⃣ 正在點火 AI Core 邊緣運算核心 (YOLOv8s-Pose)..."
echo "---------------------------------------------------------------------"
echo "🟢 點火全部完成！現在請用手機直接掃描螢幕上的 QR Code 連線。"
echo "💡 欲關閉整個專案，請直接在此終端機按下 [Ctrl + C]。"
echo "---------------------------------------------------------------------"

# 自動幫你打開 Mac 瀏覽器看 Streamlit，你連網頁都不用自己點！
open http://localhost:8501

# 最後讓 AI 核心留在這個唯一畫面上運作，方便你即時觀看跌倒通報日誌
python ai_core/detector.py

# ---------------------------------------------------------------------
# 💀 善後處理：當你在終端機按下 Ctrl+C 結束 AI 時，自動收尾背景所有服務
# ---------------------------------------------------------------------
echo -e "\n👋 偵測到關閉訊號，正在安全釋放背景所有記憶體與連線..."
kill -9 $(lsof -t -i:8000) 2>/dev/null
kill -9 $(lsof -t -i:8501) 2>/dev/null
pkill ngrok
echo "✨ 專案已安全關閉。辛苦了，Albert！"

#./start_project.sh